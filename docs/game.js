function shuffle(state, arr) {
    for(let i = arr.length - 1; i > 0; i--){
        const j = int(state, arr.length);
        if (i != j) {
            const t = arr[i];
            arr[i] = arr[j];
            arr[j] = t;
        }
    }
    return arr;
}
