function duration(seconds) {
    if (seconds < 60) {
        const v = seconds;
        return Number(seconds).toFixed(0) + ' \u{441}';
    } else if (seconds < 60 * 60) {
        const v = seconds / 60;
        return Number(v).toFixed(0) + ' \u{445}\u{432}';
    } else if (seconds < 60 * 60 * 24) {
        const v = seconds / 60 / 60;
        return Number(v).toFixed(v < 10 ? 1 : 0) + ' \u{433}\u{43e}\u{434}';
    } else if (seconds < 60 * 60 * 24 * 30) {
        const v = seconds / 60 / 60 / 24;
        return Number(v).toFixed(v < 10 ? 1 : 0) + ' \u{434}';
    } else if (seconds < 60 * 60 * 24 * 365.25) {
        const v = seconds / 60 / 60 / 24 / 30;
        return Number(v).toFixed(v < 10 ? 1 : 0) + ' \u{43c}\u{456}\u{441}';
    } else {
        const v = seconds / 60 / 60 / 24 / 365.25;
        return Number(v).toFixed(v < 10 ? 1 : 0) + ' \u{440}';
    }
}
function gold(value) {
    const c = value % 100;
    value -= c;
    value /= 100;
    const s = value % 100;
    value -= s;
    value /= 100;
    const g = value;
    const p = [];
    if (value > 0) {
        p.push(number(value) + '\u{437}');
    }
    if (s > 0) {
        p.push(s + '\u{441}');
    }
    if (c > 0) {
        p.push(c + '\u{43c}');
    }
    return p.length > 0 ? p.join(' ') : '0\u{43c}';
}
